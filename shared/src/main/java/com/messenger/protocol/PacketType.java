package com.messenger.protocol;

public enum PacketType {

    // ─── Аутентификация ──────────────────────────────────────────────────────
    LOGIN_REQUEST,       // Клиент → Сервер: логин + пароль
    LOGIN_RESPONSE,      // Сервер → Клиент: успех/ошибка + объект User
    REGISTER_REQUEST,    // Клиент → Сервер: регистрация
    REGISTER_RESPONSE,   // Сервер → Клиент: успех/ошибка

    // ─── Сообщения ───────────────────────────────────────────────────────────
    SEND_MESSAGE,        // Клиент → Сервер: отправить сообщение
    MESSAGE_BROADCAST,   // Сервер → Клиент: новое сообщение в канале
    CHANNEL_HISTORY,     // Сервер → Клиент: история сообщений

    // ─── Каналы и серверы ────────────────────────────────────────────────────
    JOIN_CHANNEL,        // Клиент → Сервер: подписаться на канал
    LEAVE_CHANNEL,       // Клиент → Сервер: отписаться от канала
    GET_SERVERS,         // Клиент → Сервер: получить список серверов
    SERVERS_LIST,        // Сервер → Клиент: список серверов пользователя
    GET_CHANNELS,        // Клиент → Сервер: получить каналы сервера
    CHANNELS_LIST,       // Сервер → Клиент: список каналов
    CREATE_SERVER,       // Клиент → Сервер: создать сервер
    CREATE_CHANNEL,      // Клиент → Сервер: создать канал
    JOIN_SERVER,         // Клиент → Сервер: вступить по invite-коду
    GET_MEMBERS,         // Клиент → Сервер: получить участников сервера
    MEMBERS_LIST,        // Сервер → Клиент: список участников

    // ─── Файлы ───────────────────────────────────────────────────────────────
    FILE_UPLOAD,         // Клиент → Сервер: загрузить файл
    FILE_DOWNLOAD,       // Клиент → Сервер: скачать файл
    FILE_DATA,           // Сервер → Клиент: данные файла

    // ─── Системные ───────────────────────────────────────────────────────────
    DISCONNECT,          // Клиент → Сервер: отключиться
    ERROR,               // Сервер → Клиент: сообщение об ошибке
    PING,                // Проверка соединения
    EVENT, COMMAND_RESPONSE, COMMAND, PONG                 // Ответ на PING
}
